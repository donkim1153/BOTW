package botw.domain;

public class Item {
	
	private int id;
	private String itemName;
	private String itemDescription;
	private String[] itemLocation;
	
	public int getId() {
		return id;
	}
	
	public String getItemName() {
		return itemName;
	}
	
	public String getItemDescription() {
		return itemDescription;
	}
	
	public String[] getItemLocation() {
		return itemLocation;
	}

}
